function Armor(params) {
    Item.call(this, params);
    params = params || {};

    this.attack = params.attack || 1;
    this.value = params.value || 1;
    this.modifiers = params.modifiers;
    this.aura = params.aura;
}

inherits(Armor, Item);
module.exports = Armor;
